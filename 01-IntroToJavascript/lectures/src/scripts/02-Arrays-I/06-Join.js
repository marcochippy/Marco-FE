const array = ["apple", "banana", "cherry", "date"];

// Create different strings by joining the array elements with various delimiters
// Default delimiter (comma)
arrayComma = array.join();
// Using dash as delimiter
arrayDash = array.join("-");
// Using space as delimiter;
arraySpace = array.join(" ");
// Using ' and ' as delimiter
arrayAnd = array.join(" and ");
// Without any delimiter
arrayNo = array.join("");

// Logs
console.log(arrayComma);
console.log(arrayDash);
console.log(arraySpace);
console.log(arrayAnd);
console.log(arrayNo);
